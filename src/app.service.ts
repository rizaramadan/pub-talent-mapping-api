import { Injectable, Inject, Logger } from '@nestjs/common';
import { QueryArrayConfig, QueryArrayResult, Pool } from "pg";
import { ApiProperty } from '@nestjs/swagger';

@Injectable()
export class AppService {
  private readonly logger = new Logger(AppService.name)
  constructor(@Inject('DATABASE_POOL') private readonly pool: Pool) {}

  async getResult(userId: string): Promise<GetResultRow> {
    const client = await this.pool.connect();
    try {
      const result = await getResult(client, { userId });
      this.logger.log(result);
      return result;
    } finally {
      client.release();
    }
  }

  async submitResult(userId: string, body: SubmitArgs): Promise<SubmitRow> {
    const client = await this.pool.connect();
    try {
      this.logger.log(body);
      const result = await submit(client,  body);
      return result;
    } finally {
      client.release();
    }
  }
}

