const getResultQuery = `-- name: GetResult :one
SELECT
        user_id,
        full_name,
        final_result,
        p.personality_role_name,
        p.cluster,
        outgoing,
        solitary,
        conceptual,
        practical,
        empathetic,
        logical,
        organized,
        flexible
    FROM talent_mapping.view_final_result v
        left join talent_mapping.personality_roles p on v.final_result = p.typology
    WHERE user_id =$1 LIMIT 1`;

interface GetResultArgs {
    userId: string;
}

export interface GetResultRow {
    userId: string;
    fullName: string;
    finalResult: string;
    personalityRoleName: string | null;
    cluster: string | null;
    outgoing: number;
    solitary: number;
    conceptual: number;
    practical: number;
    empathetic: number;
    logical: number;
    organized: number;
    flexible: number;
}

async function getResult(client: Client, args: GetResultArgs): Promise<GetResultRow | null> {
    const result = await client.query({
        text: getResultQuery,
        values: [args.userId],
        rowMode: "array"
    });
    if (result.rows.length !== 1) {
        return {
            userId: '',
            fullName: '',
            finalResult: '',
            personalityRoleName: '',
            cluster: '',
            outgoing: 0,
            solitary: 0,
            conceptual:0,
            practical: 0,
            empathetic: 0,
            logical: 0,
            organized: 0,
            flexible: 0
        };
    }
    const row = result.rows[0];
    return {
        userId: row[0],
        fullName: row[1],
        finalResult: row[2],
        personalityRoleName: row[3],
        cluster: row[4],
        outgoing: row[5],
        solitary: row[6],
        conceptual: row[7],
        practical: row[8],
        empathetic: row[9],
        logical: row[10],
        organized: row[11],
        flexible: row[12]
    };
}
